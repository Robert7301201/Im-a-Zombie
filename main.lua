	if CollidePlayer( player.X, player.Y+player.H, player.S, 0, dt ) == true or CollidePlayer( player.X+player.W, player.Y+player.H, player.S, 0, dt ) == true or CollidePlayer( player.X, player.Y, player.S, 0, dt ) == true or CollidePlayer( player.X+player.W, player.Y, player.S, 0, dt ) == true then
	else
	player.X = player.X + player.S*dt
	end
end

if love.keyboard.isDown("a") then
	if CollidePlayer( player.X, player.Y+player.H, -player.S, 0, dt ) == true or CollidePlayer( player.X+player.W, player.Y+player.H, -player.S, 0, dt ) == true or CollidePlayer( player.X, player.Y, -player.S, 0, dt ) == true or CollidePlayer( player.X+player.W, player.Y, -player.S, 0, dt ) == true then
	else
	player.X = player.X - player.S*dt
	end
end

if CollidePlayer( player.X, player.Y+player.H, 0, player.yVel, dt ) == true or CollidePlayer( player.X+player.W, player.Y+player.H, 0, player.yVel, dt ) == true then
	player.canJump = true
	player.yVel = 0
	elseif CollidePlayer( player.X, player.Y, 0, player.yVel, dt ) == true or CollidePlayer( player.X+player.W, player.Y, 0, player.yVel, dt ) == true then
	player.yVel = 0
	else
	player.Y = player.Y + player.yVel*dt
	player.canJump = false
end

if love.keyboard.isDown("w") then
jump()
end

if love.keyboard.isDown("r") then
player.X = 0
player.Y = 0
player.yVel = 0
player.xVel = 0
end

if love.keyboard.isDown("g") then
GenMap()
end

end
