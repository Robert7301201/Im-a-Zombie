function SaveMap( name )
local MapTest = love.filesystem.newFile( name..".txt")
local MapString = "map={\r\n{"

	for y=1, MapHeight do
		for x=1, MapWidth do
			if x == MapWidth then
				if y == MapHeight then
				MapString = MapString.. tostring(map[y][x]) .." },\r\n"
				else
				MapString = MapString.. tostring(map[y][x]) .." },\r\n{"
				end
			else
			MapString = MapString.. tostring(map[y][x]) ..", "
			end
		end
	end
	MapString = MapString.. "\r\n};"
	MapString = MapString.. "\r\nplayer.X = ".. tostring(player.X)
	MapString = MapString.. "\r\nplayer.Y = ".. tostring(player.Y)
	MapString = MapString.. "\r\nplayer.xVel = ".. tostring(player.xVel)
	MapString = MapString.. "\r\nplayer.yVel = ".. tostring(player.yVel)
	love.filesystem.write( name..".txt", MapString, #MapString)
end

function LoadMap( name )
love.filesystem.load( name..".txt")()
end

function GenMap()

	for y=1, MapHeight do
		for x=1, MapWidth do
			map[y][x] = 0
		end
	end

	local b = 6 + math.random()
	local f = 2 + math.random()
	
	for x=1, MapWidth do
		local gen = math.floor(f*math.sin(x/8+math.sin(b*x)))+16
		if IsBlock( x, gen) then
		map[gen][x] = 1
			for y=1, table.getn( map ) - gen do
				if IsBlock( x, gen+y ) then
					local rndm = math.random()*4
					local agen = math.cos(x+rndm)/math.sin(y+rndm)
					if agen > 0.65+y/10 or agen < -0.65+y/10 then
						if y>4 then
							map[gen+y][x] = 5
							else
							map[gen+y][x] = 2
						end
						else
						map[gen+y][x] = 2
					end
				end
			end
		end
	end
end

