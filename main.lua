function IsBlock( x, y )
	local b = false
	if y <= table.getn( map ) and y>=1 then
		if x <= table.getn( map[y] ) and x>=1 then
			b = true
		end
	end
	return b
end

function love.mousepressed( x, y, button )
	x = (x-400)/25+player.X+1
	y = (y-300)/25+player.Y+1
	
	if button=="l" then
		if IsBlock( math.floor(x), math.floor(y) ) == true then
		map[math.floor(y)][math.floor(x)]=0
		end
	elseif button=="r" then
		if IsBlock( math.floor(x), math.floor(y) ) == true then
		map[math.floor(y)][math.floor(x)]=1
		end
	end
end

