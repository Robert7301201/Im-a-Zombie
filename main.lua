	for x=1, 33 do
		local y = math.floor(y+player.Y)-12
		local x = math.floor(x+player.X)-16
		if IsBlock( x, y ) then
			if map[y][x] > 0 then
			
				if map[y][x] == 1 then
				love.graphics.setColor( 0, 200, 0, 255 )
				elseif map[y][x] == 2 then
				love.graphics.setColor( 128, 64, 64, 255 )
