				love.graphics.setColor( 255, 255, 255, 100)
				elseif map[y][x] == 4 then
				love.graphics.setColor( 195, 65, 0, 255 )
				elseif map[y][x] >4 then
				love.graphics.setColor( 100, 100, 100, 255 )
				end
			
			love.graphics.rectangle("fill", (25*x)-25, (25*y)-25, 25, 25)
			
			end
		end
	end
end

