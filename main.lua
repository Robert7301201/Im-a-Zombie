function CollidePlayer( X, Y, Xv, Yv, dt ) --Can also be used to collide any point
	local collide = false
	if IsBlock( math.floor(X+Xv*dt), math.floor(Y+Yv*dt) )==true then
		if map[math.floor(Y+Yv*dt)][math.floor(X+Xv*dt)]>0 then
			collide = true
		end
	end
	return collide
end

